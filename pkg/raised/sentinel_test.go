func TestSentinel_WithTrailingUnderscore(t *testing.T) {
	// ERROR(n_) is the pattern used by the CLI code generator to mark
	// autogenerated codes with a class prefix; the trailing underscore
	// must be stripped like any other underscore separator.
	s := NewSentinel("ERROR(10_) autogenerated code")

	if s.Code() != 10 {
		t.Errorf("expected Code() == 10, got %d", s.Code())
	}
	want := "ERROR(10) autogenerated code"
	if s.Error() != want {
		t.Errorf("expected Error() == %q, got %q", want, s.Error())
	}
}

