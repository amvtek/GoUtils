package raised

import (
	"testing"
)

func TestSentinel_NoCode(t *testing.T) {
	msg := "something went wrong"
	s := NewSentinel(msg)

	if s.Code() != 0 {
		t.Errorf("expected Code() == 0, got %d", s.Code())
	}
	if s.Error() != msg {
		t.Errorf("expected Error() == %q, got %q", msg, s.Error())
	}
}

